package com.practise.linkedin.set3;

public class Program47_UnaryOperator {

	public static void main(String[] args) {
		int firstValue = 30;
		int incrementedValue = ++firstValue;
		System.out.println("Incremented value ==>"+ incrementedValue);
		int secondValue = 56;
		int decrementedValue = --secondValue;
		System.out.println("decrementedValue value ==>"+ decrementedValue);

	}

}
